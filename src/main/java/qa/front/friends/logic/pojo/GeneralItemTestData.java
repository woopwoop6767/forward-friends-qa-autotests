package qa.front.friends.logic.pojo;

public class GeneralItemTestData {

    private String itemName;
    private String color;
    private String volume;

    public GeneralItemTestData(String itemName) {
        this.itemName = itemName;
    }


    public String getItemName() {
        return itemName;
    }

    public GeneralItemTestData setColor(String color) {
        this.color = color;
        return this;
    }
    public String getColor() {
        return color;
    }

    public GeneralItemTestData setVolume(String volume) {
        this.volume = volume;
        return this;
    }
    public String getVolume() {
        return volume;
    }









}
